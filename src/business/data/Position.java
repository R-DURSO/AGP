package business.data;

public class Position {
	private float x;
	private float y;

	public Position(float f, float g) {
		this.x = f;
		this.y = g;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	public void Position(float x , float y) {
		this.x = x;
		this.y = y;
	}

}
